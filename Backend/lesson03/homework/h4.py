
# 4. Uchta sonning o‘rtachasini topish
# Berilgan: a = 4, b = 6, c = 8 Natija: 6.0

a = float(input("Birinchi sonni kiriting: "))
b = float(input("Ikkinchi sonni kiriting: "))
c = float(input("Uchinchi sonni kiriting: "))
yigindi = a + b + c
urta_arifmetik = yigindi / 3
print(f"{a}, {b} va {c} sonlarining o‘rtacha arifmetik qiymati: {urta_arifmetik}")
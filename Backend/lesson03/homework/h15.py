
# 15. Uchburchak yuzini topish
# Berilgan: asos = 4, balandlik = 6 Natija: 12

asos = float(input("Uchburchak asosini kiriting: "))
balandlik = float(input("Uchburchak balandligini kiriting: "))
yuz = (asos * balandlik) / 2
print(f"Asosi {asos}, balandligi {balandlik} bo'lgan uchburchak yuzi {yuz}ga teng.")